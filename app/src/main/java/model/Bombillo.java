package model;

import com.example.bombiahorro.R;

public class Bombillo {

    private int bombi;
    private int mariposa;
    private int golpe;

    public Bombillo() {

        bombi = R.drawable.bombihueco;
        mariposa = R.drawable.mariposa;
        golpe = R.drawable.golpe;
    }

    public int getBombi() {

        return bombi;
    }

    public int getMariposa() {

        return mariposa;
    }

    public int getGolpe() {

        return golpe;
    }

}
